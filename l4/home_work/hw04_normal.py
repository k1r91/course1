def write_file(n=2500, fname="data/numbers.txt"):
    '''
    Пишем в файл n случайных чисел от 0 до 9
    :param n:
    :param fname:
    :return:
    '''
    with open(fname, 'w', encoding='utf-8') as f:
        for i in range(n):
            f.write(str(random.randrange(10)))


def get_sequence(fname="data/numbers.txt"):
    #получаем файл в список
    with open(fname, 'r', encoding='utf-8') as f:
        num_list = []
        while True:
            c = f.read(1)
            if not c:
                break
            num_list.append(int(c))
    i = 0
    sequence = []
    #получаем все последовательности одинаковых чисел
    while i < len(num_list) - 1:
        to_add = ''
        while num_list[i] == num_list[i + 1]:
            to_add += str(num_list[i])
            i += 1
            if i >= len(num_list) - 1:
                break
            if num_list[i] != num_list[i + 1]:
                to_add += str(num_list[i])
                break
        if to_add:
            sequence.append(to_add)
        i += 1
    result = []
#   сортируем последовательности по длине
    sequence.sort(key=lambda x: len(x), reverse=True)
    i = 0

#    добавлеям первую последовательность

    result.append(sequence[0])

#    если есть последовательности такой же длины, как и первая, то добавляем и их

    while len(sequence[i]) == len(sequence[i + 1]):
        result.append(sequence[i + 1])
        i += 1
    return result
